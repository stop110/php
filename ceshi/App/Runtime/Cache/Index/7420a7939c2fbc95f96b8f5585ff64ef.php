											<p>　　<strong>&ldquo;作为一个共产党员，就应该为党工作，活到老、学到老&rdquo;</strong></p>
											<p>　　1957年龚全珍跟随甘祖昌将军回家乡江西省莲花县，将军扛起锄头当农民，将军夫人发挥专业特长当上了乡村教师。当老师是她这辈子最乐意做的事情，她把心思都花在青少年的培养上。即使离休后，仍然关心着下一代的成长，走进学校、机关、部队、工厂，义务对青少年开展革命传统教育。30多年间，没有人记得她去了多少学校，讲过多少课，但都记得，她从不要一分钱报酬，还经常自带馒头，就着白开水作午餐。她是一位好老师，兢兢业业地履行教书育人的职责，并把这个当成兑现自己入党誓言的最好实践。</p>
											<p>　　她说：&ldquo;别看我做了大半辈子老师，其实我一直把自己当学生。党员的崇高信仰永远不能丢。&rdquo;即使现在脑子退化，不听使唤了，也不能马马虎虎过下去，&ldquo;作为一个共产党员，就应该为党工作，活到老、学到老&rdquo;。</p>
											<p>　　<strong>&ldquo;我的身份并不特殊，但却有一份特殊的责任&rdquo;</strong></p>
											<p>　　龚全珍有时候会笑称，自己是从令人羡慕的将军夫人，变成了扎根农村的老太太。转头又说：&ldquo;其实，我的身份并不特殊，但却有一份特殊的责任，那就是把祖昌永葆本色、一心为民、无私奉献的精神传承下去。&rdquo;</p>
											<p>　　为了这份特殊的责任，她总是把身边群众的困难当作自己的疾苦，尽己所能帮助有困难的人；为了这份特殊的责任，她教子从严，树好家风，从来不向组织上提要求，7个孩子谁也没沾过&ldquo;将军后代&rdquo;的光；为了这份特殊的责任，耄耋之年又和社区居委会的同志一道办起了&ldquo;龚全珍工作室&rdquo;，每周都会来到工作室，与群众沟通交流，帮助他们解决思想和生活上的问题。多年来，龚全珍一直保持着简朴的生活作风，工资的绝大部分都拿去资助学生，做公益，一生淡泊名利。</p>
											<p>　　<strong>&ldquo;我还要努力，还要学习，争取做个优秀的共产党员&rdquo;</strong></p>
											<p>　　龚全珍1937年参加革命工作，1949年加入中国共产党。离休后，每次离退休老干部集中学习讨论，不管刮风下雨，龚全珍从未缺席。现在，她仍然坚持每天都读书看报，了解新闻和各种知识。已经90多岁的龚老说：&ldquo;我还要努力，还要学习，争取做个优秀的共产党员。&rdquo;（共产党员网记者　阮玉秀）</p>
											<p>　　<strong>延伸阅读：</strong></p>
											<p style="text-align: center;">　<strong>　从将军夫人到农民媳妇</strong></p>
											<p>　　翻开龚全珍的日记本，偶尔出现一两首雪莱的诗。她平日里喜欢看书，尤其小说。即使现在年纪大了，依然保持着一个&ldquo;文艺青年&rdquo;的习惯。从小生长在城市，受过高等教育，没有去过农村。那一年，甘祖昌将军连续三年申请回乡务农的报告批准下来后，别人问她：&ldquo;甘祖昌回老家当农民，你去当什么？&rdquo;龚全珍说：&ldquo;他当农民我也当农民，不会当学着当。&rdquo;</p>
											<p style="text-align: justify;">　　从祖国的大西北到千里之外的陌生农村，环境、生活方式，甚至语言都完全不一样。从将军夫人一下子变成了农民媳妇。起初也有过困惑，但是理解了丈夫的初心后，她便坚定追随至今。一个腰里插根旱烟筒，一个腋下夹着课本，日出而作，日落而息，相互扶持，一生清贫亦无悔。也许这就是爱情应该有的样子，相濡以沫，相伴一生。　　</p>
